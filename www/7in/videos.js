function addNewVideos(loop) {
  delay = defDelay; 
  let video = newVideos[loop];
  
  if ((typeof video === "undefined") || (typeof video['url'] === "undefined")) {
    window.console.log("Finished ################################################################################"); 
    return; 
  }
  
  let vUrl = video['url'];
  let vType = "fi";
  if (vUrl.indexOf("youtube.com") >= 0) { 
    vType = "yt";
    vUrl = vUrl.substring(vUrl.indexOf('=') + 1);
  }
  
  let vTitle = video['title'];
  if ((typeof vTitle === 'undefined') || (vTitle.length < 1)) { vTitle = vUrl; }
  
  let uid = findVideo(vUrl); 
  if (uid >= 0) { 
    window.console.log("Duplicate: " + vTitle); 
    delay = 10; 
  } else {
    window.console.log("Adding: " + vTitle); 
    socket.emit("queue", { id: vUrl, title: vTitle, pos: "end", type: vType, "temp": false });
  } 
 
  setTimeout(()=>{ loop++; addNewVideos(loop); }, delay); 
} 

